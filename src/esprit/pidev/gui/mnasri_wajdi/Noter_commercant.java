    private void btm_noteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btm_noteActionPerformed

       

        Commercant c = new Commercant();

        Commercant l = liste.get(cb_nom_commercant.getSelectedIndex());
        
        
        float note_ancienne;
        int nbr_note_nouveau;
        float somme_note_nouveau;
        float note_nouveau;
        int nbr_note_ancienne;
        float somme_note_ancienne;
        note_ancienne = l.getNote();
        nbr_note_ancienne = l.getNbr_note();
        somme_note_ancienne = l.getSomme_note();
        int id__commercant =l.getId_commercant();



        note_nouveau = (Float.parseFloat(tf_note.getValue().toString()) + somme_note_ancienne) / (nbr_note_ancienne + 1);

        nbr_note_nouveau = nbr_note_ancienne + 1;
        somme_note_nouveau = somme_note_ancienne + Float.parseFloat(tf_note.getValue().toString());

        DealDAO deal_dao = new DealDAO();

        c.setNote(note_nouveau);
        c.setNbr_note(nbr_note_nouveau);
        c.setSomme_note(somme_note_nouveau);
        c.setId_commercant(l.getId_commercant());
        
        int tf___client=Integer.parseInt(tf__client.getText());
        
        if(deal_dao.allverif(tf___client,id__commercant)){
        deal_dao.updateNoteCommercant(c);

       
        Verif v = new Verif();
        v.setId_client(tf___client);
        v.setId_commercant(id__commercant);
        deal_dao.insertVerif(v);
        
        DealDAO deal_DAO = new DealDAO();
        liste = new ArrayList<Commercant>();
        liste = deal_DAO.allcommercant();
        Commercant cc = new Commercant();

        Commercant ll = liste.get(cb_nom_commercant.getSelectedIndex());

        tf_nbr_note.setText(Integer.toString(ll.getNbr_note()));
        tf_note_actuel.setText(Float.toString(ll.getNote()));
        
        JOptionPane.showMessageDialog(this, "Ajout de note au commercant effectué avec succès");
        }
        else
        {JOptionPane.showMessageDialog(this, "Vous avez déja noter ce commercant");}




    }//GEN-LAST:event_btm_noteActionPerformed


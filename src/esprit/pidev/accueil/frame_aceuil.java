    public List affichercommercant() throws SQLException {
        List<Commercant> listcommercant = new ArrayList<Commercant>();

        String requete = "select * from commercant";
        try {
            Statement statement = MyConnection.getInstance()
                    .createStatement();
            ResultSet resultat = statement.executeQuery(requete);

            while (resultat.next()) {
                Commercant commercant2 = new Commercant();
                commercant2.setLogin(resultat.getString(1));
                commercant2.setPassword(resultat.getString(2));

                listcommercant.add(commercant2);
            }
            return listcommercant;
        } catch (SQLException ex) {
            //Logger.getLogger(PersonneDao.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("erreur lors du chargement des depots " + ex.getMessage());
            return null;
        }
    }

   



